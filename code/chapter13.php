        <h5 style="color: #39CCCC">Um erro terrível</h5>
        <p class="text-justify">Começaram a interceptar varias mensagens com o mesmo indicador, chamado de “depths” (profundezas). Então os alemães estavam enviado uma mensagem muito longa, cerca de 4 mil caracteres, então o operado pediu para enviar novamente a mensagem e o segundo operador enviou novamente a mesma mensagem, e a soma das mensagens iguais se cancelariam, enviaram esse texto cifrado para Tilman que descobriu que o inicio da mensagem começava com SPRUCHNUMBER (numero da mensagem), e na segunda enviou como SPRUCHNR, abreviado, essa pequena diferença permitiu que Tilman decifrasse o texto original.</p>
