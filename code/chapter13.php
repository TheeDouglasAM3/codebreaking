        <p class="text-justify">No verão de 1942, Alan Turing ja quase uma lenda em Bletchley Park, foi encarragado da Tunny, com lapis papel e borracha, usou o processo de Disjunção exclusiva, turing desenhou folhas coim possiveis confiugurações da maquina e então foi utilizando palpites para verificar se estão certos.</p>
        <p class="text-justify">Esse metodo, chamado de turingueria ou turinguismo, permitiu que Bleatchley Parck ler mensagens secretas assinadas pelo proprio Hitler, em 1943 durante a batalha de Kursk, as mensagens decifradas foram enviadas para os russos descrevendo todo o movimento do exercito alemão. O exercito vermelho conseguiu avançar quase sem oposição para berlim e sem perceber que os britanicos tinha decifrado as mensagens da Lorenz começaram a usa-la.</p>

        <h5 style="color: #39CCCC">A testery</h5>
        <p class="text-justify">Era preciso gerenciar o alto numero de mensagens decifradas em tempo de guerra, para isso criaram o grupo Testery, Max Newman, tutor de Turing, acreditou que esse serviço poderia ser feito por maquinas eletronicas. A maquina criada foi chamada de “Heath Robison”, cartunista que desenhava maquinas incrivelmente complexas para realizar atividades simples. Porem a maquina nao estava apresentando muitos problemas principalmente por causa do aquecimento.</p>
