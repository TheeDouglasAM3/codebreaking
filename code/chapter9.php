		<p class="text-justify">No dia 4 de março de 1941 um navio alemão foi capturado com as configurações do mês de fevereiro, embora desatualizado foram uteis para as atualizações na enigma. com a decifração dos códigos notaram que tinham navios meteorológicos alemães que poderiam ter maquinas enigmas, o palpite estava certo e encontraram os códigos do mês seguinte.</p>
