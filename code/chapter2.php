        <p>Anagrama é reconstruir um texto a partir de pistas encontradas com a análise de frequência, os estudiosos árabes foram os primeiros a descobrir a possibilidade de desvendar cifras calculando frequência de letras, não apenas isoladas, mas pares e até mesmo trios de letras.</p>

        <h5 style="color: #bf3fc8">Abu Al-khalil</h5>

        <p>Nasceu em Omã, deu aulas em Baçorá, durante um período foi secretário do Vizir, se convertendo para o islamismo sunita. escreveu o "livro da letra", considerado o primeiro dicionário árabe. A relatos que sua morte aconteceu quando estava pensando em um sistema, para que sua criada não ser enganada por merceeiros, distraído caiu do pilar da mesquita.</p>
    
        <p>Também escreveu: livro da linguagem secreta, atualmente perdido, se interessou por cifras quando o imperador bizantino pediu para decifrar uma mensagem em grego. Al-khalil supôs que a carta devia começar com:"em nome de Deus" e com isso descobriu as primeiras letras e com elas foi decifrando o restante da mensagem.</p>
