        <h5 style="color: #bf3fc8">Qalqashandi</h5>

        <p>Em 1412 o egípcio ahmad al-Qalqashandi terminou o: "alvorada para cegos" incluindo uma seção sobre criptografia, descrevendo 7 tipos de criptografia:</p>

        <ol>
            <li>Substituir uma letra por outra;</li>
            <li>Escrever a palavra de trás para frente</li>
            <li>Inverter letras alternadas</li>
            <li>Usar o valor numérico das letras</li>
            <li>Substituições múltiplas</li>
            <li>Substituir letras por nomes</li>
            <li>Substituir letras por símbolos</li>
        </ol>

        <p>Qalqashandi defendia que a criptoanálise tem que conhecer a língua que foi escrita o criptograma. ele avaliou a frequência de letras do Corão e viu que era diferente de outros textos, então avaliou que para começar a avaliar um texto é preciso contar o número de letras e quantas vezes cada símbolo aparece, depois disso descobrir qual é o símbolo que separa as palavras, depois de ter encontrado o espaço, procure os símbolos que mais se repetem e suponha que seja as letras que mais se repetem, e depois o segundo símbolo que mais se repete e analisando se é possível construir palavras.</p>

        <p>Entretanto o método de Qalqashandi nao se espalharam rapidamente pelo mundo muçulmano, em 1600 o sultão de marrocos enviou seu secretário para negociar com a Elizabeth I da inglaterra, contra a espanha, mas o destinatário da mensagem levou 15 anos para entender a mensagem, mas se tiver utilizado o método de Qalqashandi levaria apenas algumas horas.</p>
